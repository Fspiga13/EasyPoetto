package com.easypoetto.model;

public class Reservation {
	
	private int id;
	private int quantity;
	
	
	public Reservation(int id, int quantity) {
		this.id = id;
		this.quantity = quantity;
	}


	public int getId() {
		return id;
	}


	public int getQuantity() {
		return quantity;
	}

	
	
	
	

}
