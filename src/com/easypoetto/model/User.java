package com.easypoetto.model;

public class User {
	
	private String email;
	private String password;
	private int role;
	
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}

	/**
	 * @return the role
	 */
	public int getRole() {
		return role;
	}

}
